# Program: Demonstrate creation of variables and different data types

# Integer
age = 20
print("Age:", age, "| Type:", type(age))

# Float
height = 5.8
print("Height:", height, "| Type:", type(height))

# String
name = "Karan"
print("Name:", name, "| Type:", type(name))

# Boolean
is_student = True
print("Is Student:", is_student, "| Type:", type(is_student))

# List
fruits = ["Apple", "Banana", "Mango"]
print("Fruits:", fruits, "| Type:", type(fruits))

# Tuple
coordinates = (10.5, 20.8)
print("Coordinates:", coordinates, "| Type:", type(coordinates))

# Set
unique_numbers = {1, 2, 3, 3, 2}
print("Unique Numbers:", unique_numbers, "| Type:", type(unique_numbers))

# Dictionary
person = {"name": "Karan", "age": 20, "city": "Surat"}
print("Person Details:", person, "| Type:", type(person))

# None Type
nothing = None
print("Nothing:", nothing, "| Type:", type(nothing))
